/** Strip leading/trailing punctuation and trim for glossary lookup. */
export function normalizeGreekForGlossary(token: string): string {
  return token.replace(/^[\s.,;:·;\u037e\u2013\u2014\u2019]+|[\s.,;:·;\u037e\u2013\u2014\u2019]+$/g, '').trim()
}

